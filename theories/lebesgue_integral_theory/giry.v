Lemma forall_subset_eq {T} (A B : set_system T) :
  sigma_algebra [set: T] A -> sigma_algebra [set: T] B ->
  (forall Z, sigma_algebra [set: T] Z -> A `<=` Z <-> B `<=` Z)
